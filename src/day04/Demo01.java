package day04;

public class Demo01 {
	public static void main(String[] args) {
		// 取余数运算 %
		
		//5 % 2 = 得2余1  结果是 1
		System.out.println(5%2);//1
		System.out.println(0%2);//得0余0 结果0
		System.out.println(1%2);//得0余1 结果1
		System.out.println(2%2);//得1余0 结果0
		System.out.println(3%2);//得1余1 结果1
		System.out.println(4%2);//得2余0 结果0
		
		int index = 0;
		System.out.println(index%8);
	}
}
